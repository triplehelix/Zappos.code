<strong>Zombie Survival Tips</strong><br />
<hr />
<p>&nbsp;Below are some simple tips to remember in the case of a Zombie Apocalypse. Remember these when the world nears it's end and you could be preserving the existence of the human race!
</p><br /><br />


<div class="grid_full space">
	<div class="grid_1">
        <strong>Tip #1: Always be skeptical</strong><br />
        <hr />
        <p>&nbsp;Be careful who you trust. There is no laws now to punish people who take advantage of others
        </p>
    </div>
    
	<div class="grid_1">
        <strong>Tip #2: Always have a plan</strong><br />
        <hr />
        <p>&nbsp;Never do anything risky without knowing what you will do next no matter what outcome you end up in. Being prepared for all situations and having a goal to complete will keep you from cornering yourself in a back alley.
        </p>
    </div>
    
	<div class="grid_1">
        <strong>Tip #3: Don't Get Attached</strong><br />
        <hr />
        <p>&nbsp;There may come a time when you have to kill a zombie that was formally your friend or loved one. You have to understand before getting into that situation that you are not killing the person you care about but a evil being that has occupied their brain.
        </p>
    </div>
</div>

<div class="grid_full space">
	<div class="grid_1">
        <strong>Tip #4: Be careful what you eat</strong><br />
        <hr />
        <p>&nbsp;Food goes bad, and zombie farmers aren't going to grow you new crops. You don't want to be caught on the crapper by a zombie after a meal of old cheese.
        </p>
    </div>
    
	<div class="grid_1">
        <strong>Tip #5: Avoid Cities</strong><br />
        <hr />
        <p>&nbsp;Densely populated areas should always be avoided at all costs. even and experienced zombie killer is at a severe risk of being devoured when facing large hoards of zombies.
        </p>
    </div>
    
	<div class="grid_1">
        <strong>Tip #6: Don't give up hope</strong><br />
        <hr />
        <p>&nbsp;Try to stay positive and remember that you are still alive. You're already doing better than most people (now zombies). Don't give up hope, you can survive and prevent the extinction of humankind.
        </p>
    </div>
</div>